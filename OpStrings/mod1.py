def maxLen(String1,String2):
    if len(String1)>len(String2):
        print(f"A primeira String é maior com: {len(String1)}")
    else:
        print(f"A segunda String é maior com:{len(String2)} ")

def minLen(String1,String2):
    if len(String1)<len(String2):
        print(f"A primeira String é menor com: {len(String1)}")
    else:
        print(f"A segunda String é menor com: {len(String2)}")